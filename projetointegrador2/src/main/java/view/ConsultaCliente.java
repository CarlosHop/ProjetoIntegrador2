    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       DefaultTableModel tabelaCliente = (DefaultTableModel) jTable1.getModel();
       tabelaCliente.setRowCount(0);
   
       
       String nomeBusca = txtNome.getText();
       String cpfBusca = jftCpf.getText().replace(".","").replace("-","").replace(" ","");
       
       
        if(nomeBusca.equals("") && cpfBusca.equals("")){
           try {
               recarregarTabela();
           } catch (SQLException ex) {
               Logger.getLogger(ConsultaCliente.class.getName()).log(Level.SEVERE, null, ex);
           }
       }else if(!cpfBusca.equals("")){
             
           try {
               ArrayList<String[]> listaCliente = clienteController.consultaCpf(cpfBusca);
               for(String[] cliente : listaCliente)
                   tabelaCliente.addRow(cliente);
                   
                   
                   } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null, "Erro na consulta");
           }
       }else if(!nomeBusca.equals("")){
            System.out.println("Entrou Por nome"); 
           try {
               ArrayList<String[]> listaCliente = clienteController.consultaNome(nomeBusca);
               for(String[] cliente : listaCliente)
                   tabelaCliente.addRow(cliente);
                   
                   
                   } catch (SQLException ex) {
               JOptionPane.showMessageDialog(null, "Erro na consulta");
           }
       
       }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(jTable1.getSelectedRow()>=0){
            int linha = jTable1.getSelectedRow();
            int id = Integer.parseInt(String.valueOf(jTable1.getValueAt(linha, 0)));
           
            try {
                if(clienteController.excluir(id)){
                    JOptionPane.showMessageDialog(null, "Excluis√£o bem sucedida");
                    recarregarTabela(); 
                }else{
                    JOptionPane.showMessageDialog(null, "Erro na exclus√£o");
                }
            } catch (Exception ex) {
                Logger.getLogger(ConsultaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
        JOptionPane.showMessageDialog(null, "Cliente n√£o selecionado para exclui");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(jTable1.getSelectedRow()>=0){
            int indice =jTable1.getSelectedRow();
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            int ID = Integer.parseInt(modelo.getValueAt(indice,0).toString());
            try {
                CadastroDeCliente edita = new CadastroDeCliente(ID);
                edita.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(ConsultaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado para altera√ß√£o");
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    public void recarregarTabela() throws SQLException{
        ArrayList<String[]> listaRetorno = clienteController.consulta();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        for (String[] cliente : listaRetorno) {
            modelo.addRow(cliente);
        }
    }
